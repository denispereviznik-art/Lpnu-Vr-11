import numpy as np

# створюємо два одновимірні масиви
a = np.array([1, 2, 3, 4, 5])
b = np.array([6, 7, 8, 9, 10])

print("Масив A:", a)
print("Масив B:", b)

# арифметичні операції
print("\nАрифметичні операції:")
print("A + B =", a + b)
print("A - B =", a - b)
print("A * B =", a * b)
print("A / B =", a / b)

# об’єднання масивів (конкатенація)
c = np.concatenate((a, b))
print("\nНовий масив C (A об’єднано з B):", c)

# знаходження характеристик нового масиву
max_el = np.max(c)
min_el = np.min(c)
sum_el = np.sum(c)
prod_el = np.prod(c)

print("\nРезультати операцій з масивом C:")
print("Максимальний елемент:", max_el)
print("Мінімальний елемент:", min_el)
print("Сума елементів:", sum_el)
print("Добуток елементів:", prod_el)

# пояснення (коментарі)
print("\nКоментарі:")
print("- Масиви A і B були створені за допомогою np.array.")
print("- Виконано всі арифметичні операції між ними.")
print("- Новий масив C отримано шляхом конкатенації A і B.")
print("- Для C знайдено максимум, мінімум, суму та добуток.")
